// what is enum and why we are use it and when we use it?
// An enum (short for "enumeration") is a distinct data type that consists of a set of named 
//values called "enumerators". Enums are used to represent a collection of related constants 
//in a more readable and maintainable way. They provide a way to define a variable that can 
//only take one of a predefined set of values, which can improve code clarity and reduce errors.
public enum VehicleSize
{
    Motorcycle = 1,
    Car = 2,
    Truck = 3
}